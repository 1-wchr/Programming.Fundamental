#include <stdio.h>
int main()
{
    int n1, n2, result;
    printf("Enter number1: ");
    scanf("%d", &n1);
    printf("Enter number2: ");
    scanf("%d", &n2);

    if (result = n1 + n2)
    {
        printf("ans1 = %d\n", result);
    }
    if (result = n1 - n2)
    {
        printf("ans2 = %d\n", result);
    }
    if (result = n1 * n2)
    {
        printf("ans3 = %d\n", result);
    }
    if (result = n1 / n2)
    {
        printf("ans4 = %d\n", result);
    }

    return 0;
}